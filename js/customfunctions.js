function function1() {
  document.getElementById("nav1").classList.add("active");
  document.getElementById("nav2").classList.remove("active");
  document.getElementById("nav3").classList.remove("active");
  document.getElementById("nav4").classList.remove("active");
  var w = document.getElementById("biocontainer");
  var x = document.getElementById("contactcontainer");
  var y = document.getElementById("aboutcontainer");
  var z = document.getElementById("homecontainer");
  if (z.style.display === "none") {
    z.style.display = "block";
  } else {
    z.style.display = "block";
  }
  if (w.style.display === "block") {
    w.style.display = "none";
  } else {
    w.style.display = "none";
  }
  if (x.style.display === "block") {
    x.style.display = "none";
  } else {
    x.style.display = "none";
  }

  if (y.style.display === "block") {
    y.style.display = "none";
  } else {
    y.style.display = "none";
  }
}
